CREATE OR ALTER PROCEDURE PRC_PROCESSAR_PEDIDOS
AS
BEGIN
    SET NOCOUNT ON;

    DECLARE
        @V_IDPEDIDO VARCHAR(50),
        @V_IDPRODUTO INT,
        @V_QTD_NECESSARIA INT,
        @V_ESTOQUE INT,
        @V_ATENDER BIT;

    SELECT 'PEDIDOS A PROCESSAR NA PROCEDURE' AS ETAPA, ID_PEDIDO, VL_TOTAL, STATUS
    FROM PEDIDOS_PED
    WHERE STATUS IS NULL OR STATUS = 'PENDENTE'
    ORDER BY VL_TOTAL DESC, DATA_PEDIDO ASC;

    DECLARE C_PEDIDOS CURSOR LOCAL FAST_FORWARD FOR
        SELECT ID_PEDIDO
        FROM PEDIDOS_PED
        WHERE STATUS IS NULL OR STATUS = 'PENDENTE'
        ORDER BY VL_TOTAL DESC, DATA_PEDIDO ASC;

    OPEN C_PEDIDOS;
    FETCH NEXT FROM C_PEDIDOS INTO @V_IDPEDIDO;

    WHILE @@FETCH_STATUS = 0
    BEGIN
        SET @V_ATENDER = 1;

        /* Remove solicitacoes antigas desse pedido para recalcular a falta atual. */
        DELETE FROM COMPRAS_COM
        WHERE ID_PEDIDO = @V_IDPEDIDO
          AND STATUS_COMPRA = 'ABERTA';

        DECLARE C_ITENS_VERIFICACAO CURSOR LOCAL FAST_FORWARD FOR
            SELECT
                I.ID_PRODUTO,
                SUM(I.QTD) AS QTD_NECESSARIA
            FROM ITPEDIDO_ITP I
            WHERE I.ID_PEDIDO = @V_IDPEDIDO
            GROUP BY I.ID_PRODUTO;

        OPEN C_ITENS_VERIFICACAO;
        FETCH NEXT FROM C_ITENS_VERIFICACAO INTO @V_IDPRODUTO, @V_QTD_NECESSARIA;

        WHILE @@FETCH_STATUS = 0
        BEGIN
            SELECT @V_ESTOQUE = P.QTD_ESTOQUE
            FROM PRODUTOS_PRD P
            WHERE P.ID_PRODUTO = @V_IDPRODUTO;

            IF ISNULL(@V_ESTOQUE, 0) < @V_QTD_NECESSARIA
            BEGIN
                SET @V_ATENDER = 0;

                INSERT INTO COMPRAS_COM (
                    ID_PEDIDO,
                    ID_PRODUTO,
                    QTD_SOLICITADA
                )
                VALUES (
                    @V_IDPEDIDO,
                    @V_IDPRODUTO,
                    @V_QTD_NECESSARIA - ISNULL(@V_ESTOQUE, 0)
                );
            END;

            FETCH NEXT FROM C_ITENS_VERIFICACAO INTO @V_IDPRODUTO, @V_QTD_NECESSARIA;
        END;

        CLOSE C_ITENS_VERIFICACAO;
        DEALLOCATE C_ITENS_VERIFICACAO;

        IF @V_ATENDER = 1
        BEGIN
            /* Registra a movimentacao antes de debitar o estoque. */
            INSERT INTO MOVIMENTO_MOV (
                ID_PEDIDO,
                ID_PRODUTO,
                QTD_MOVIMENTADA,
                ESTOQUE_ANTES,
                ESTOQUE_DEPOIS
            )
            SELECT
                @V_IDPEDIDO,
                P.ID_PRODUTO,
                X.QTD_NECESSARIA,
                P.QTD_ESTOQUE,
                P.QTD_ESTOQUE - X.QTD_NECESSARIA
            FROM PRODUTOS_PRD P
            INNER JOIN (
                SELECT
                    I.ID_PRODUTO,
                    SUM(I.QTD) AS QTD_NECESSARIA
                FROM ITPEDIDO_ITP I
                WHERE I.ID_PEDIDO = @V_IDPEDIDO
                GROUP BY I.ID_PRODUTO
            ) X
                ON X.ID_PRODUTO = P.ID_PRODUTO;

            /* Debita o estoque. */
            UPDATE P
            SET P.QTD_ESTOQUE = P.QTD_ESTOQUE - X.QTD_NECESSARIA
            FROM PRODUTOS_PRD P
            INNER JOIN (
                SELECT
                    I.ID_PRODUTO,
                    SUM(I.QTD) AS QTD_NECESSARIA
                FROM ITPEDIDO_ITP I
                WHERE I.ID_PEDIDO = @V_IDPEDIDO
                GROUP BY I.ID_PRODUTO
            ) X
                ON X.ID_PRODUTO = P.ID_PRODUTO;

            UPDATE PEDIDOS_PED
            SET STATUS = 'ATENDIDO',
                DATA_ATUALIZACAO_STATUS = GETDATE()
            WHERE ID_PEDIDO = @V_IDPEDIDO;

            SELECT 'PEDIDO ATENDIDO' AS ETAPA, @V_IDPEDIDO AS ID_PEDIDO;
        END
        ELSE
        BEGIN
            UPDATE PEDIDOS_PED
            SET STATUS = 'PENDENTE',
                DATA_ATUALIZACAO_STATUS = GETDATE()
            WHERE ID_PEDIDO = @V_IDPEDIDO;

            SELECT 'PENDENTE - COMPRA GERADA' AS ETAPA, @V_IDPEDIDO AS ID_PEDIDO;
        END;

        FETCH NEXT FROM C_PEDIDOS INTO @V_IDPEDIDO;
    END;

    CLOSE C_PEDIDOS;
    DEALLOCATE C_PEDIDOS;

    SELECT 'RESUMO DE STATUS DOS PEDIDOS APOS PROCESSAMENTO' AS ETAPA, STATUS, COUNT(*) AS QTD_PEDIDOS
    FROM PEDIDOS_PED
    GROUP BY STATUS;

    SELECT 'ESTOQUE APOS PROCESSAMENTO' AS ETAPA, SKU, NOME_PRODUTO, QTD_ESTOQUE
    FROM PRODUTOS_PRD
    ORDER BY ID_PRODUTO;
END;

GO
CREATE OR ALTER PROCEDURE PRC_CARREGAR_ENTREGA_FORNECEDOR
AS
BEGIN
    SET NOCOUNT ON;

    DROP TABLE IF EXISTS #ENTREGA_ENT;

    CREATE TABLE #ENTREGA_ENT (
        SKU VARCHAR(50),
        QTD_ENTREGUE INT
    );

    BULK INSERT #ENTREGA_ENT
    FROM 'C:\5SBD\AV1\CARGA_FORNECEDOR.csv'
    WITH (
        FORMAT = 'CSV',
        FIRSTROW = 2,
        FIELDQUOTE = '"',
        ROWTERMINATOR = '0x0a',
        CODEPAGE = '65001'
    );

    SELECT 'PRODUTOS RECEBIDOS DO FORNECEDOR' AS ETAPA, *
    FROM #ENTREGA_ENT;

    UPDATE P
    SET P.QTD_ESTOQUE = P.QTD_ESTOQUE + E.QTD_ENTREGUE
    FROM PRODUTOS_PRD P
    INNER JOIN #ENTREGA_ENT E
        ON E.SKU = P.SKU;

    SELECT 'ESTOQUE APOS ENTRADA DO FORNECEDOR' AS ETAPA, P.SKU, P.NOME_PRODUTO, P.QTD_ESTOQUE
    FROM PRODUTOS_PRD P
    INNER JOIN #ENTREGA_ENT E
        ON E.SKU = P.SKU
    ORDER BY P.ID_PRODUTO;

    UPDATE C
    SET C.STATUS_COMPRA = 'RECEBIDA'
    FROM COMPRAS_COM C
    INNER JOIN PRODUTOS_PRD P
        ON P.ID_PRODUTO = C.ID_PRODUTO
    INNER JOIN #ENTREGA_ENT E
        ON E.SKU = P.SKU
    WHERE C.STATUS_COMPRA = 'ABERTA';

    SELECT 'COMPRAS ATUALIZADAS APOS ENTREGA DO FORNECEDOR' AS ETAPA, C.*
    FROM COMPRAS_COM C
    INNER JOIN PRODUTOS_PRD P
        ON P.ID_PRODUTO = C.ID_PRODUTO
    INNER JOIN #ENTREGA_ENT E
        ON E.SKU = P.SKU
    ORDER BY C.ID_COMPRA;
END;
GO

