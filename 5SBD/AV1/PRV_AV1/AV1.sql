    CONSTRAINT FK_ITPEDIDO_PEDIDOS
        FOREIGN KEY (ID_PEDIDO) REFERENCES PEDIDOS_PED(ID_PEDIDO),

    CONSTRAINT FK_ITPEDIDO_PRODUTOS
