CREATE TABLE PRODUTOS_PRD (
    ID_PRODUTO INT IDENTITY(1,1) PRIMARY KEY,
    SKU VARCHAR(50) NOT NULL,
    UPC VARCHAR(50) NULL,
    NOME_PRODUTO VARCHAR(200) NOT NULL,
    QTD_ESTOQUE INT NOT NULL CONSTRAINT DF_PRODUTOS_QTD_ESTOQUE DEFAULT 0,
    CONSTRAINT UK_PRODUTOS_SKU UNIQUE (SKU),
    CONSTRAINT CK_PRODUTOS_QTD_ESTOQUE CHECK (QTD_ESTOQUE >= 0)
